package engine;

public class PointClass {
	int x;
	int y;
	public PointClass(int x,int y){
		this.x=x;
		this.y=y;
	}
	public int returnX() {
		return this.x;
	}
	public int returnY() {
		return this.y;
	}
	public void setX(int x) {
		this.x=x;
	}
	public void setY(int y) {
		this.y=y;
	}
}

